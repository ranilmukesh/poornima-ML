def load_diabetes_dataset():
    """Load the most complete diabetes dataset"""
    print("🔍 Searching for diabetes datasets...")

    base_paths = ['./final_imputed_data/', 'final_imputed_data/', './']
    dataset_files = [
        'nmbfinalDiabetes (4)_selected_columns_cleaned_processed_final_imputed.csv',
        'nmbfinalnewDiabetes (3)_selected_columns_cleaned_processed_final_imputed.csv',
        'PrePostFinal (3)_selected_columns_cleaned_processed_final_imputed.csv'
    ]

    target_column = 'PostBLHBA1C'

    for file in dataset_files:
        for bp in base_paths:
            path = os.path.join(bp, file)
            if os.path.exists(path):
                df = pd.read_csv(path)
                if target_column in df.columns:
                    # Clean target variable
                    initial_rows = len(df)
                    df = df.dropna(subset=[target_column])

                    # Remove extreme outliers only (>4 standard deviations)
                    mean_val = df[target_column].mean()
                    std_val = df[target_column].std()
                    z_scores = np.abs((df[target_column] - mean_val) / std_val)
                    df = df[z_scores <= 4]

                    print(f"✅ Loaded: {file}")
                    print(f"📊 Shape: {df.shape} (removed {initial_rows - len(df)} outliers)")
                    print(f"📈 HbA1c range: {df[target_column].min():.2f} - {df[target_column].max():.2f}")
                    print(f"📊 HbA1c mean ± std: {df[target_column].mean():.2f} ± {df[target_column].std():.2f}")

                    return df, target_column

    raise FileNotFoundError("❌ No suitable diabetes dataset found!")

