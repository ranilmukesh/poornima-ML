def create_medical_features(df, target_col):
    """Create advanced features for HbA1c prediction using medical domain knowledge"""
    print("🧬 Starting Advanced Feature Engineering...")
    df_enhanced = df.copy()

    # Separate numeric and categorical features
    numeric_cols = df_enhanced.select_dtypes(include=[np.number]).columns.tolist()
    if target_col in numeric_cols:
        numeric_cols.remove(target_col)

    print(f"📊 Found {len(numeric_cols)} numeric features for enhancement")

    # 1. Create Medical Risk Scores
    if len(numeric_cols) >= 3:
        print("🏥 Creating diabetes risk composite scores...")
        # Find features most correlated with HbA1c
        correlations = df_enhanced[numeric_cols].corrwith(df_enhanced[target_col]).abs()
        top_features = correlations.nlargest(5).index

        for i, feat in enumerate(top_features[:3]):
            df_enhanced[f'diabetes_risk_score_{i+1}'] = df_enhanced[feat]

    # 2. Statistical Interactions for Top Predictors
    print("🔬 Creating feature interactions...")
    corr_with_target = df_enhanced[numeric_cols].corrwith(df_enhanced[target_col]).abs()
    high_corr_features = corr_with_target[corr_with_target > 0.2].index.tolist()

    if len(high_corr_features) >= 2:
        for i, feat1 in enumerate(high_corr_features[:3]):
            for feat2 in high_corr_features[i+1:4]:
                # Multiplicative interaction
                df_enhanced[f'{feat1}_x_{feat2}'] = df_enhanced[feat1] * df_enhanced[feat2]

                # Ratio interaction (clinical ratios are important)
                denominator = df_enhanced[feat2].replace(0, 1e-8)
                df_enhanced[f'{feat1}_div_{feat2}'] = df_enhanced[feat1] / denominator

    # 3. Non-linear Transformations for Top Features
    print("📈 Creating non-linear transformations...")
    for feat in high_corr_features[:3]:
        df_enhanced[f'{feat}_squared'] = df_enhanced[feat] ** 2
        df_enhanced[f'{feat}_sqrt'] = np.sqrt(np.abs(df_enhanced[feat]))
        df_enhanced[f'{feat}_log1p'] = np.log1p(np.abs(df_enhanced[feat]))

    # 4. Statistical Aggregate Features
    print("📊 Creating statistical aggregates...")
    current_numeric = df_enhanced.select_dtypes(include=[np.number]).columns
    if target_col in current_numeric:
        current_numeric = current_numeric.drop(target_col)

    if len(current_numeric) > 0:
        df_enhanced['feature_mean'] = df_enhanced[current_numeric].mean(axis=1)
        df_enhanced['feature_std'] = df_enhanced[current_numeric].std(axis=1)
        df_enhanced['feature_max'] = df_enhanced[current_numeric].max(axis=1)
        df_enhanced['feature_min'] = df_enhanced[current_numeric].min(axis=1)
        df_enhanced['feature_range'] = df_enhanced['feature_max'] - df_enhanced['feature_min']

    # 5. Clean up infinite and NaN values
    print("🧹 Cleaning enhanced features...")
    df_enhanced = df_enhanced.replace([np.inf, -np.inf], np.nan)

    # Corrected line to fill NaNs only in numeric columns
    df_enhanced = df_enhanced.fillna(df_enhanced.median(numeric_only=True))

    features_added = df_enhanced.shape[1] - df.shape[1]
    print(f"✨ Feature Engineering Complete!")
    print(f"📈 Features: {df.shape[1]} → {df_enhanced.shape[1]} (+{features_added} new features)")

    return df_enhanced

